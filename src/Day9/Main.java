package Day9;

public class Main {
    public static void main(String[] args) {
        // greater 0 , equals to 0, less than 0
        int number = -5;
        if(number > 0){
            System.out.println(number +" is greater than 0");
        }
        else if(number == 0){
            System.out.println(number +" equals to 0");
        }
        else{
            System.out.println(number + " less than 0");
        }
    }
}
